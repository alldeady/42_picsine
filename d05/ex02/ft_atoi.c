int	ft_atoi(char *str)
{
	int res;
	int sign;

	res = 0;
	sign = 1;
	while (*str == '\n' || *str == ' ' || *str == '\t' ||
			*str == '\r' || *str == '\v' || *str == '\f')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign = 0;
		str++;
	}
	while ((*str) >= '0' && (*str) <= '9')
	{
		res *= 10;
		if (sign)
			res += (*str - '0');
		else
			res -= (*str - '0');
		str++;
	}
	return (res);
}