void	ft_putchar(char c);

int		main(int argc, char *argv[])
{
	int i;
	int j;

	if (1 < argc)
	{
		i = argc - 1;
		while (1 <= i)
		{
			j = 0;
			while (argv[i][j] != '\0')
			{
				ft_putchar(argv[i][j]);
				j++;
			}
			ft_putchar('\n');
			i--;
		}
	}
	return (0);
}
