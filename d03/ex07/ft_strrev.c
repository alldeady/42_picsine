char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	int		p;
	char	t;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	i--;
	p = 0;
	p = i / 2;
	while (i > p)
	{
		t = str[i];
		str[i] = str[j];
		str[j] = t;
		i--;
		j++;
	}
	return (str);
}
